                    <h1>Keywords</h1>
                    <p>In C++, keywords are reserved words that have a specific meaning and usage. They are essential building blocks for writing code</p>
                    <h3>What are keywords in C++? </h3>
                    <ul>
                        <li>They are predefined identifiers with special meanings</li>
                        <li>They cannot be used as identifiers or variable names</li>
                        <li>They are critical to program structure and functionality</li>
                        <li>They are used to define data types, control flow, and object-oriented notions</li>
                    </ul>
                    <h3>How are keywords used in C++?</h3>
                    <ul type="square">
                        <li>The while keyword starts a loop that lasts as long as a condition is true </li>
                        <li>The break keyword is used to break the statement </li>
                        <li>The sizeof keyword measures the amount of memory used by a variable or data type </li>
                        <li>The struct keyword defines a structure with public members and base classes by default </li>
                    </ul>
