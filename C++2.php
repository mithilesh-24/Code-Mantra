                    <h2>Object based programming</h2>
                    <p><b>Object based programming </b>is a paradigm that implements some features of object oriented programming but not all.</p>
                    <p>In object based programming, data and its associated meaningful functions are enclosed in one single entity called class.</p>
                    <p>Classes enforce information hiding and abstraction thereby separating the implementation details.</p>
                    <p>In object based programming, whenever there is any change in the definition of type, user's interface remains unaffected generally.</p>
                    <p>The object based programming can be thought of as a subset of object oriented programming as it implements some of the features implemented by object oriented programming like information hiding, abstraction, classes, function overloading, etc.</p>
                    <p>The object based programming does not implement inheritance and polymorphism.</p>
                    <p>The advantages of object based programming are:</p>
                    <ul>
                        <li>it overcomes most shortcomings of procedural programming</li>
                        <li>it localizes changes and hides implementation details from user</li>
                        <li>it supports user-defined types</li>
                        <li>implements information hiding and abstraction</li>
                    </ul>
                    <p>However, object based programming suffers from one major limitation and that is its inability to represent real world relationships that exist among objects.</p>
                    <p><b>Object oriented programming paradigm </b>is a superset of object based programming. It offers all the features of object based programming and overcomes its limitation by implementing inheritance.</p>
