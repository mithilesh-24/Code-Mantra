                    <h2>Understanding Data abstraction and encapsulation</h2>
                    <p><b>The object oriented programming </b>evolved to overcome the limitations of procedural programming approach.</p>
                    <h3>The fundamental concepts in OOP are:</h3>
                    <ol>
                        <li>Data Abstraction</li>
                        <li>Data Encapsulation</li>
                        <li>Modularity</li>
                        <li>Inheritance</li>
                        <li>Polymorphism</li>
                    </ol>
                    <p><b>Data abstraction </b>refers to the act of representing essential features without including the background details and explanations.</p>
                    <p>Let us consider an example of a switch board, you can only press certain switches according to your requirement. What is happening inside, how it is happening, you need not know.</p>
                    <p>This is <b>abstraction</b>, you know only essential things to operate a switch board without knowing the background details of a switch board.</p>
                    <p><b>Data encapsulation </b>is combining both data and functions that operate on the data under a single unit.</p>
                    <p>Here, data is not accessed to the outside world directly and can only be accessed through member functions.</p>
                    <p>The insulation of data from direct access by the program is called data hiding.</p>
                    <p><b>Abstraction </b>and <b>encapsulation</b> are complementary concepts, where abstraction focuses upon the observable behavior of an object and encapsulation focuses upon the implementation that gives rise to this behavior.</p>
                    <p><b><i>Encapsulation </i></b>is most often achieved through information hiding, which is the process of hiding all the details of an object that do not contribute to its essential characteristics. The structure of an object is hidden as well as the implementation of its methods.</p>
