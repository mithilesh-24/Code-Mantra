                    <h2>Understanding Procedural Progamming</h2>
                    <p><strong> programming paradigm </strong>defines the methodology of designing and implementing programs using the key features and other building blocks of a programming language.</p>
                    <p><b>A programming paradigm </b>gives you an idea how problems are generally analyzed and solved in a particular programming language.</p>
                    <p>Below are a few commonly used programming paradigms :</p>
                    <ol>
                        <li>Procedural Programming</li>
                        <li>Object Based Programming</li>
                        <li>Object Oriented Programming</li>
                    </ol>
                    <p><b>Procedural programming </b>lays more emphasis on procedure than data. C language uses procedural programming style.</p>
                    <p>C++ and Java can also be used as procedural programming languages with some enhanced features such as type checking, reference variables, inline functions, default arguments etc.</p>
                    <h2>Below are a few pros and cons of procedural programming languages:</h2>
                    <h3>Pros</h3>
                    <ul>
                        <li>For smaller programs the code is usually small, more readable and result oriented.</li>
                        <li>They are usually very efficient since they are written for a very specific purpose.</li>
                    </ul>
                    <h3>Cons</h3>
                    <ul>
                        <li>Since programs are function based they do not relate to a real life object.</li>
                        <li>When large amount of code is written inside functions, it becomes very difficult to maintain or make modifications.</li>
                        <li>Changing the way data is stored can require large amount of changes in code, unlike in Object oriented programs, making it difficult to maintain large systems.</li>
                    </ul>
